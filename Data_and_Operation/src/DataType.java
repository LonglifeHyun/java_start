public class DataType{
	 public static void main(String[] args) {
		 System.out.println(5);			// Number
		 System.out.println("Five");	// String
		 System.out.println("5"); 		// String
		 
		 System.out.println(5+5); 		// Number
		 System.out.println("5"+ "5"); 	// String
		 
		 System.out.println(5*5);		// Number
//		 System.out.println("5"*"5");	// Error
		 
		 System.out.println("55555".length());
	 }
}